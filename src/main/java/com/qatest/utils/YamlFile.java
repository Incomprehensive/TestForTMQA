package com.qatest.utils;

public class YamlFile {

    // Create settings that represent config.yaml

    private String url;
    private String email;
    private String pass;

    /**
     * Getter for URL
     * @return
     */
    public String getUrl() {
        return url;
    }

    /**
     * Getter for email
     * @return
     */
    public String getEmail() {
        return email;
    }

    /**
     * Getter for pass
     * @return
     */
    public String getPass() {
        return pass;
    }
}
